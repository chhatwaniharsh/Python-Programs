#Creating List in Python
list1=[10,20.8,"Harsh",True]
print(list1)
print(type(list1))

#Using Append Method
list1.append(36)
print(list1)

#Using Insert Method
list1.insert(1,"Veer")
print(list1)

#Using Remove Method
list1.remove(20.8)
print(list1)

#Using Extend Method
list2=["Hello",False,85]
list1.extend(list2)
print(list1)

#Using Pop Method
list1.pop()
print(list1)
